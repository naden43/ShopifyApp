import Foundation

struct Products: Codable {
    let products: [Product]
}

// MARK: - Product
struct Product: Codable {
    let id: Int
    let title, bodyHTML, vendor, productType: String
    //let createdAt: Date
    let handle: String
    //let updatedAt, publishedAt: Date
    let templateSuffix: JSONNull?
    let publishedScope, tags, status, adminGraphqlAPIID: String
    let variants: [Variant]
    let options: [Option]
    let images: [Image]
    let image: Image

    enum CodingKeys: String, CodingKey {
        case id, title
        case bodyHTML = "body_html"
        case vendor
        case productType = "product_type"
        //case createdAt = "created_at"
        case handle
        //case updatedAt = "updated_at"
        //case publishedAt = "published_at"
        case templateSuffix = "template_suffix"
        case publishedScope = "published_scope"
        case tags, status
        case adminGraphqlAPIID = "admin_graphql_api_id"
        case variants, options, images, image
    }
}

// MARK: - Image
struct Image: Codable {
    let id: Int
    let alt: JSONNull?
    let position, productID: Int
    //let createdAt, updatedAt: Date
    let adminGraphqlAPIID: String
    let width, height: Int
    let src: String
    let variantIDS: [JSONAny]

    enum CodingKeys: String, CodingKey {
        case id, alt, position
        case productID = "product_id"
       // case createdAt = "created_at"
        //case updatedAt = "updated_at"
        case adminGraphqlAPIID = "admin_graphql_api_id"
        case width, height, src
        case variantIDS = "variant_ids"
    }
}

// MARK: - Option
struct Option: Codable {
    let id, productID: Int
    let name: String
    let position: Int
    let values: [String]

    enum CodingKeys: String, CodingKey {
        case id
        case productID = "product_id"
        case name, position, values
    }
}

// MARK: - Variant
struct Variant: Codable {
    let id, productID: Int
    let title, price, sku: String
    let position: Int
    let inventoryPolicy: String
    let compareAtPrice: JSONNull?
    let fulfillmentService, inventoryManagement, option1, option2: String
    let option3: JSONNull?
  //  let createdAt, updatedAt: Date
    let taxable: Bool
    let barcode: JSONNull?
    let grams, weight: Int
    let weightUnit: String
    let inventoryItemID, inventoryQuantity, oldInventoryQuantity: Int
    let requiresShipping: Bool
    let adminGraphqlAPIID: String
    let imageID: JSONNull?

    enum CodingKeys: String, CodingKey {
        case id
        case productID = "product_id"
        case title, price, sku, position
        case inventoryPolicy = "inventory_policy"
        case compareAtPrice = "compare_at_price"
        case fulfillmentService = "fulfillment_service"
        case inventoryManagement = "inventory_management"
        case option1, option2, option3
        //case createdAt = "created_at"
        //case updatedAt = "updated_at"
        case taxable, barcode, grams, weight
        case weightUnit = "weight_unit"
        case inventoryItemID = "inventory_item_id"
        case inventoryQuantity = "inventory_quantity"
